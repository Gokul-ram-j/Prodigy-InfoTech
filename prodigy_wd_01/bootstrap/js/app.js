function makeslide(name_list){
    let elem="";
    for(nme of name_list){
        elem+=
        `
        <div class="swiper-slide">
            <img src=${nme} alt="">
            <div class="btngrp  ">
              <button class="btn btn-primary m-1 p-2" >Purchase Now <i class="fa fa-youtube" aria-hidden="true"></i></button>
              <button class="btn btn-primary m-1 p-2">Watch Trailer <i class="fa fa-external-link" aria-hidden="true"></i></button>
            </div>
          </div>
        `
    }
    return elem;
}




function makecards(name_list){
    let elem="";
    for(nme of name_list){
        elem+=
        `
        <div class="card">
        <img src=${nme} alt="">
        </div>
        `
    }
    return elem
}

